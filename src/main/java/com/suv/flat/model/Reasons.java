package com.suv.flat.model;

public class Reasons {
	int reason_id;
	String reason;
	
	public Reasons(){
		this.reason_id=-1;
	}
	
	public int getReason_id() {
		return reason_id;
	}
	public void setReason_id(int reason_id) {
		this.reason_id = reason_id;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	

}
